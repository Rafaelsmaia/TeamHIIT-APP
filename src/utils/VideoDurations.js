// Função para obter duração de um vídeo
export const getVideoDuration = (videoId) => {
  // Extrair ID do YouTube da URL se necessário
  let cleanVideoId = videoId;
  
  if (videoId && typeof videoId === 'string') {
    // Se é uma URL completa do YouTube
    if (videoId.includes('youtu.be/')) {
      cleanVideoId = videoId.split('youtu.be/')[1].split('?')[0];
    } else if (videoId.includes('youtube.com/watch?v=')) {
      cleanVideoId = videoId.split('v=')[1].split('&')[0];
    } else if (videoId.includes('youtube.com/embed/')) {
      cleanVideoId = videoId.split('embed/')[1].split('?')[0];
    }
    // Se já é um ID limpo, usar como está
  }
  
  const duration = VIDEO_DURATIONS[cleanVideoId];
  
  if (duration) {
    return duration;
  }
  
  // Se não encontrou, retornar padrão
  return '30 min';
};

// Função para calcular calorias baseada na duração
export const getCaloriesByDuration = (duration) => {
  // Extrair minutos da string "X min"
  const minutes = parseInt(duration.replace(' min', ''));
  
  if (minutes <= 10) {
    return 200;
  } else if (minutes <= 20) {
    return 250;
  } else if (minutes <= 30) {
    return 450;
  } else if (minutes <= 40) {
    return 600;
  } else {
    return 750;
  }
};

